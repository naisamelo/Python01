frase ='Curso em Vídeo Python'
print(frase[3]) #Retorna a 3 letra
print(frase[3:13]) #Da letra 3 até a 12 letra
print(frase[:13]) #Não indica o início e vai até a 12 letra
print(frase[13:]) #Não indica o final, apenas o início 
print(frase[1:15]) #Da 1 até a 14
print(frase[1:15:2]) #Do 1 até o 15 pulando de dois em dois
print(frase[1::2]) #Não indica o final, apenas o início e vai pulando de dois em dois
print(frase[::2]) #Não indicia início e nem final, apenas quantos vai pular